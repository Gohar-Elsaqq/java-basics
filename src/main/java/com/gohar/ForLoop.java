package com.gohar;

public class ForLoop {


        public static void main(String[] args) {
            for(int i = 10; i >=0; i--) {
                System.out.println("i = " + i);
            }
            System.out.println("----------for loop ----------");
            for(int i = 0; i <10; i++) {
                System.out.println("i = " + i);
            }

        }

    }


